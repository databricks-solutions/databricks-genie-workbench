# MAGIC ## The 5 Lever Categories
# MAGIC
# MAGIC Patches generated by the adaptive strategist draw from 5 lever categories. The strategist decides which levers to use per iteration based on the current failure landscape — they are **not** iterated sequentially.
# MAGIC
# MAGIC | Lever | Name | What It Optimizes | Example Patches | Risk |
# MAGIC |:-----:|------|-------------------|-----------------|:----:|
# MAGIC | 1 | **Tables & Columns** | Column descriptions, aliases, synonyms | `update_column_description`, `add_column_synonym` | 🔵 Low–Med |

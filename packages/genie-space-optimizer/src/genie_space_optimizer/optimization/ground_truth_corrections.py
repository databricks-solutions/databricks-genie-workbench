"""Row classification helpers for the ground-truth correction queue.

Benchmarks in GSO are partially synthetic — questions and their
``expected_sql`` are generated by an LLM, validated against the
warehouse, but not always hand-curated. Empirically, Genie is sometimes
*more* correct than the GT it is being scored against. The arbiter
exists specifically to adjudicate this. When the arbiter returns
``genie_correct`` while ``result_correctness=no``, the row is a
**corpus-quality signal**, not a Genie failure: the GT itself is wrong
or under-specified.

This module provides three pure helpers used by ``harness._analyze_and_distribute``
to keep corpus-defective rows out of the lever loop:

* :func:`is_gt_correction_candidate` — predicate.
* :func:`should_cluster_as_soft_signal` — guard that prevents
  ``arbiter=genie_correct`` rows from being clustered as soft signals
  via the individual-judge-failure path.
* :func:`build_gt_correction_candidate` — Delta-friendly payload
  builder for ``genie_eval_gt_correction_candidates``.

The status state machine for queued candidates:

    pending_review        — initial; reviewer has not looked.
    accepted_corpus_fix   — GT was updated; qid eligible for proactive
                            mining (Task 9 reads this status).
    rejected_keep_gt      — reviewer disagreed; treat as confirmed Genie
                            failure on next iteration. Task 8 escalates
                            these if the same root cause persists.
    superseded            — replaced by a later iteration's row for the
                            same qid.

The module is leaf-level: it imports the moved judge-failure predicate
from ``evaluation`` (not ``harness``) to avoid a circular import.
"""

from __future__ import annotations

from typing import Any

# Imported lazily inside ``should_cluster_as_soft_signal`` to keep this
# module dependency-free at import time. ``evaluation`` is heavy.


_RC_FALSE_VALUES: frozenset[str] = frozenset({"no", "false", "0", "0.0"})
_GT_CORRECTION_ARBITER: str = "genie_correct"


def _value(row: dict[str, Any], *keys: str) -> str:
    """Return the first non-None value found across *keys*, lowercased
    and stripped. Empty string when nothing matches."""
    for key in keys:
        val = row.get(key)
        if val is not None:
            return str(val).strip()
    return ""


def _result_correctness(row: dict[str, Any]) -> str:
    return _value(
        row,
        "feedback/result_correctness/value",
        "result_correctness/value",
    ).lower()


def _arbiter(row: dict[str, Any]) -> str:
    return _value(
        row,
        "feedback/arbiter/value",
        "arbiter/value",
    ).lower()


def is_gt_correction_candidate(row: dict[str, Any]) -> bool:
    """Return ``True`` when this row should be diverted to corpus review.

    Contract: ``result_correctness ∈ {"no", "false", "0", "0.0"}`` AND
    ``arbiter == "genie_correct"``. Case-insensitive on both.
    """
    return (
        _result_correctness(row) in _RC_FALSE_VALUES
        and _arbiter(row) == _GT_CORRECTION_ARBITER
    )


def should_cluster_as_soft_signal(row: dict[str, Any]) -> bool:
    """Return ``True`` when the row should enter the soft-signal cluster.

    Guard: a GT-correction candidate is *never* a soft signal even if
    individual judges disagree — the disagreement is downstream of a
    defective GT, not of a Genie issue. Otherwise defer to the same
    individual-judge-failure check used by ``has_individual_judge_failure``.
    """
    # Local import keeps this module leaf-level. ``evaluation`` is the
    # canonical home for the predicate after Task 0's relocation.
    from genie_space_optimizer.optimization.evaluation import (
        has_individual_judge_failure,
    )

    if is_gt_correction_candidate(row):
        return False
    return bool(has_individual_judge_failure(row))


def build_gt_correction_candidate(
    row: dict[str, Any],
    *,
    run_id: str,
    iteration: int,
) -> dict[str, Any]:
    """Build a Delta-friendly payload for ``genie_eval_gt_correction_candidates``.

    Status starts at ``"pending_review"``; reviewers transition it via
    the four-state machine documented at the module level.
    """
    return {
        "run_id": run_id,
        "iteration": int(iteration),
        "question_id": _value(row, "inputs.question_id", "question_id", "id"),
        "question": _value(row, "inputs.question", "question"),
        "expected_sql": _value(row, "inputs.expected_sql", "expected_sql"),
        "genie_sql": _value(
            row,
            "outputs.predictions.sql",
            "generated_sql",
            "genie_sql",
        ),
        "arbiter_verdict": _arbiter(row),
        "arbiter_rationale": _value(
            row,
            "feedback/arbiter/rationale",
            "arbiter/rationale",
        ),
        "status": "pending_review",
    }

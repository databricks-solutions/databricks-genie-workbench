# ── Table Names (not in common/config.py) ─────────────────────────────

TABLE_DATA_ACCESS_GRANTS = "genie_opt_data_access_grants"

# Task 1: Ground-truth correction queue.
# When the arbiter rules ``genie_correct`` on a row whose
# ``result_correctness`` is ``no``, the GT itself is wrong or
# under-specified; the row is parked here for human/automated review
# rather than driving the lever loop.
TABLE_GT_CORRECTION_CANDIDATES = "genie_eval_gt_correction_candidates"

# Task 3: Decision audit. Single Delta surface for every gate decision
# (acceptance, proposal grounding, reflection drops, AFS / leakage
# rejections, etc.). Carries source_cluster_ids_json and
# proposal_to_patch_map_json so a single SQL query reconstructs
# cluster -> proposal -> patch -> applied -> accepted/rolled_back.
TABLE_LEVER_LOOP_DECISIONS = "genie_eval_lever_loop_decisions"

# Task 4: Per-question pass/fail transition tracker. Persisted full-eval
# transitions so a bundle that flips a previously-passing qid from pass
# to fail rolls back regardless of aggregate direction.
TABLE_QUESTION_REGRESSIONS = "genie_eval_question_regressions"

# Task 8: Human-required escalation queue. After a cluster's signature
# accumulates ``HUMAN_REQUIRED_CONTENT_ROLLBACK_THRESHOLD`` content
# regressions in the same run, the optimizer stops proposing patches
# for it and parks an evidence record here for human review.
TABLE_HUMAN_REQUIRED = "genie_eval_human_required"

# Task 9: proactive feature-mining artifacts. Persisted per run so a
# reviewer can inspect "what did the proactive miner find on the
# passing corpus, and what enrichments did it propose?" without
# replaying the run.
TABLE_PROACTIVE_CORPUS_PROFILE = "genie_eval_proactive_corpus_profile"
TABLE_PROACTIVE_PATCHES = "genie_eval_proactive_patches"

